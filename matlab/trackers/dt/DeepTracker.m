    function [trkfileObj,tfsuccload] = hlpLoadTrk(tfile,varargin)
      [rawload,movfile,movnframes] = myparse(varargin,...
        'rawload',false,...
        'movfile','',...
        'movnframes',[] ...
        );
            
      try
        if rawload
          trkfileObj = load(tfile,'-mat');
        else
          trkfileObj = TrkFile.loadsilent(tfile,'movfile',movfile,...
            'movnframes',movnframes);
        end
        tfsuccload = true;
      catch ME
