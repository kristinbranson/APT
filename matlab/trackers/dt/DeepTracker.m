      mIdx = obj.lObj.currMovIdx;
      if isempty(mIdx) || mIdx==0 || obj.lObj.isinit
        % proj load etc
        return;
      end
      [trks,tfHasRes] = obj.getTrackingResults(mIdx);
      if tfHasRes
        %obj.trackCurrResLoadFromTrks(trks);
        trks{1}.mergeMultiView(trks{2:end});
        trk1 = trks{1};
