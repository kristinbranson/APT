        else
          iTgt = [];
        end
      else
        iTgt = lo.currTarget;
      end
      trk = obj.trkP;
      if ~isempty(iTgt) && ~isnan(iTgt) && ~isempty(trk)
        tpos = trk.getPTrkTgt(iTgt);
