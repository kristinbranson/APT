          obj.(f) = s.(f);
        else
          warningNoTrace('Ignoring field ''%s'' in save token, because no such property in class DeepTracker', f) ;
        end
      end
      
      obj.dryRunOnly = false;
      
%      obj.setHideViz(s.hideViz);
      obj.trackCurrResUpdate();
      obj.newLabelerFrame();
    end

