          else
            tfSuccess = obj.trkSpawn(backend,mIdx,tMFTConc,dlLblFileLcl,...
              cropRois,hmapArgs,f0,f1,'gpuids',gpuids,args{:});
          end
          if ~tfSuccess,
            obj.bgTrkReset();
            return;
          end

        case DLBackEnd.AWS
          % TODO: AWS gpu provisioning
          %args = {'isMultiView',isMultiViewTrack,'isSerialMultiMov',true};
          if isexternal,
            args = {};
            if obj.lObj.hasTrx,
